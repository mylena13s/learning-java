
public class Exer12 {

	public static void main(String[] args) {
		int dez = 10;
		int vinte = 20;
		
		boolean res = dez > vinte; 
		System.out.println(res);
		
		res = dez < vinte; 
		System.out.println(res);
		
		res = dez == vinte; 
		System.out.println(res);
		
		res = dez != vinte; 
		System.out.println(res);
		
		System.out.println(dez <= vinte);
		System.out.println(dez >= vinte);

	}

}
